int count(int a, int b, int c, int d)
{
    if(a==0 && b==0 ) return dp[c][d];
    else if(a==0) return dp[c][d]- dp[c][b-1];
    else if(b==0) return dp[c][d]- dp[a-1][d];
    else return dp[c][d]+ dp[a-1][b-1]- dp[a-1][d] - dp[c][b-1];
}
