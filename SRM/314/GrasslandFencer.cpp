bool vis[1<<17];
double ret[1<<17];
vector<double> f;
int n;

double area(double a, double b, double c)
{
    double p = 0.5*(a+b+c);
    return sqrt(p*(p-a)*(p-b)*(p-c));
}

double dfs(int state)
{
    double best = 0.0f;
    if(vis[state]) return ret[state];
    for(int i=0; i<n; i++)
    {
        for(int j=i+1; j<n; j++)
        {
            for(int k=j+1; k<n; k++)
            {
                if(f[k] < f[i]+f[j] && state&(1<<i) && state&(1<<j) && state&(1<<k))
                {
                    int newstate=state;
                    newstate &= ((1<<n)-1 - (1<<i));
                    newstate &= ((1<<n)-1 - (1<<j));
                    newstate &= ((1<<n)-1 - (1<<k));
                    best = max(best, area(f[i], f[j], f[k]) + dfs(newstate));
                }
            }
        }
    }
    vis[state]=1;
    ret[state] = best;
    return best;
}
